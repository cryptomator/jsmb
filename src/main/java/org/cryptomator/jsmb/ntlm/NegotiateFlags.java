package org.cryptomator.jsmb.ntlm;

/**
 * Flags used in the NEGOTIATE_MESSAGE.
 * @see <a href="https://learn.microsoft.com/en-us/openspecs/windows_protocols/ms-nlmp/99d90ff4-957f-4c8a-80e4-5bfe5a9a9832">NEGOTIATE_MESSAGE</a>
 */
public interface NegotiateFlags {

	int NTLMSSP_NEGOTIATE_56 = 0x8000_0000;
	int NTLMSSP_NEGOTIATE_KEY_EXCH = 0x4000_0000;
	int NTLMSSP_NEGOTIATE_128 = 0x2000_0000;

	int NTLMSSP_NEGOTIATE_VERSION = 0x0200_0000;
	int NTLMSSP_NEGOTIATE_TARGET_INFO = 0x0080_0000;
	int NTLMSSP_REQUEST_NON_NT_SESSION_KEY = 0x0040_0000;

	int NTLMSSP_NEGOTIATE_IDENTIFY = 0x0010_0000;
	int NTLMSSP_NEGOTIATE_EXTENDED_SESSIONSECURITY = 0x0008_0000;

	int NTLMSSP_TARGET_TYPE_SERVER = 0x0002_0000;
	int NTLMSSP_TARGET_TYPE_DOMAIN = 0x0001_0000;
	int NTLMSSP_NEGOTIATE_ALWAYS_SIGN = 0x0000_8000;

	int NTLMSSP_NEGOTIATE_OEM_WORKSTATION_SUPPLIED = 0x0000_2000;
	int NTLMSSP_NEGOTIATE_OEM_DOMAIN_SUPPLIED = 0x0000_1000;

	int NTLMSSP_NEGOTIATE_ANONYMOUS = 0x0000_0800;

	int NTLMSSP_NEGOTIATE_NTLM = 0x0000_0200;
	int NTLMSSP_NEGOTIATE_LM_KEY = 0x0000_0080;
	int NTLMSSP_NEGOTIATE_DATAGRAM = 0x0000_0040;
	int NTLMSSP_NEGOTIATE_SEAL = 0x0000_0020;
	int NTLMSSP_NEGOTIATE_SIGN = 0x0000_0010;

	int NTLMSSP_REQUEST_TARGET = 0x0000_0004;

	int NTLMSSP_NEGOTIATE_OEM = 0x0000_0002;
	int NTLMSSP_NEGOTIATE_UNICODE = 0x0000_0001;
}
